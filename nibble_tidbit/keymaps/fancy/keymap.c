bool process_record_user(uint16_t keycode, keyrecord_t *record) {
#ifdef OLED_ENABLE
	zero_wpm_cycles = 0;
	//and syncronize it with another half since process_record_user works only on master
	transaction_rpc_send(RPC_ID_USER_ZERO_WPM_CYCLES_SYNC, sizeof zero_wpm_cycles, &zero_wpm_cycles);
#endif

    tap_dance_action_t *action;
    switch (keycode) {
        case TD(TD_F1):  // list all tap dance keycodes with tap-hold configurations
        case TD(TD_F2):
        case TD(TD_F3):
        case TD(TD_F4):
        case TD(TD_F5):
        case TD(TD_F6):
        case TD(TD_F7):
        case TD(TD_F8):
        case TD(TD_F9):
        case TD(TD_F10):
        case TD(TD_F11):
        case TD(TD_F12):
		case TD(TD_BSPC):
			action = &tap_dance_actions[QK_TAP_DANCE_GET_INDEX(keycode)];
			tap_dance_tap_hold_t *tap_hold = (tap_dance_tap_hold_t *)action->user_data;

			if (record->event.pressed) {
				tap_hold->mods = get_mods();
			} else if (action->state.count && !action->state.finished) {
				tap_code16(apply_mods16(tap_hold->tap, tap_hold->mods));
				tap_hold->mods = 0;
            }
    }

	return true;
}

