static void render_space(void) {
#ifdef WPM_ENABLE
    char wpm = get_current_wpm();
#else
    char wpm = 0;
#endif
    char render_row[128];
    int row,i;
	
	if (zero_wpm_cycles >= 128*3) {
		return;
	} else if (wpm == 0) {
		zero_wpm_cycles++;
	}

	for (row=0; row<4; row++) {
		oled_set_cursor(0,row);

		unsigned int wpm_shift = wpm/4;
		for(i=0; i<128; i++) {
			unsigned int wpm_offset = i-wpm_shift;
			render_row[i] = pgm_read_byte(pgm_read_ptr(&(space_rows[row]))+i+state);
			if (i>=wpm_shift && wpm_offset < 34) {
				render_row[i] = (render_row[i] & pgm_read_byte(pgm_read_ptr(&(mask_rows[row])) + wpm_offset))
					| pgm_read_byte(pgm_read_ptr(&(ship_rows[row])) + wpm_offset);
			}
		};

		oled_write_raw(render_row, 128);
	}

    state = (state + 1 + (wpm/15)) % (128*2);
	
	oled_render_dirty(false); //call with 'true' for smoother animation, but encoder might skip pulses in this case
}

bool oled_task_user(void) {
	if (!is_keyboard_left()) {
		render_space();
	}
    return false;
}

void zero_wpm_cycles_sync(uint8_t initiator2target_buffer_size, const void *initiator2target_buffer, uint8_t target2initiator_buffer_size, void *target2initiator_buffer) {
	zero_wpm_cycles = *(unsigned int *) initiator2target_buffer;
}

void keyboard_post_init_user() {
	transaction_register_rpc(RPC_ID_USER_ZERO_WPM_CYCLES_SYNC, zero_wpm_cycles_sync);
}
